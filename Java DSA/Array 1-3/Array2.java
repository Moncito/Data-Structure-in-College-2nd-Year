public class Array2 {

    public static void main(String[] args) {

        // Create an Array
        int[] age = { 12, 4, 5 };

        // Loop Through Array Using Loop

        System.out.println("Using Loop");
        for (int i = 0; i < age.length; i++) {

            System.out.println(age[i]);
        }

    }

}