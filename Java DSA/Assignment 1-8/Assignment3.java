public class Assignment3 {

    public static void main(String[] args) {

        int a = 20;
        int b = 6;

        int sum = a + b;
        int prod = a - b;
        int qout = a / b;
        int rem = a + b;

        System.out.println(a + "+" + b + "=" + sum);
        System.out.println(a + "-" + b + "=" + prod);
        System.out.println(a + "/" + b + "=" + qout);
        System.out.println(a + "%" + b + "=" + rem);
        System.out.println(a + "=" + qout + "*" + b + "+" + rem);

    }

}

